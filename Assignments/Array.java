public class Array {
       public static void main(String[] args) {

       String [] cars = {"Volvo", "Toyota", "Camry", "Benz",};  




       for (int counter = 0; counter < cars.length; counter++) {

           if (counter == 1) {
           System.out.println(cars[1] + " is my favorite car");
           }
           else 
           System.out.println(cars[counter]);
   }   
 }
}